public class Main {

    public static void main(String[] args) {
        System.out.println("Hallo Welt");
        countToTen();

        }

    public static void countToTen() {
        int number=0;
        while (number < 11) {
            System.out.println("Die aktuelle Zahl ist " + number);
            number++;
        }
    }

}


